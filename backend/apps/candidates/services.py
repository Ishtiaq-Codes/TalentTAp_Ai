def parse_resume(candidate_id):
    """
    Synchronous task to extract data from a candidate's resume using Groq or Gemini.
    """
    from apps.candidates.models import CandidateProfile, CandidateSkill, Experience, Education, Certification
    import apps.core.llm as llm
    import json
    import logging
    import time
    
    logger = logging.getLogger(__name__)
    
    try:
        profile = CandidateProfile.objects.get(id=candidate_id)
        if not profile.resume:
            return
            
        file_path = profile.resume.path
        text = ''
        
        if getattr(llm, 'HAS_GROQ', False):
            # Read PDF locally for Groq
            try:
                import fitz # PyMuPDF
                doc = fitz.open(file_path)
                for page in doc:
                    text += page.get_text() + '\n'
                doc.close()
            except ImportError:
                try:
                    import PyPDF2
                    with open(file_path, 'rb') as f:
                        reader = PyPDF2.PdfReader(f)
                        for page in reader.pages:
                            page_text = page.extract_text()
                            if page_text:
                                text += page_text + '\n'
                except Exception as e:
                    logger.error(f"PyPDF2 Extraction Error: {e}")
                    raise Exception("Failed to extract text from PDF for Groq")
            except Exception as e:
                logger.error(f"PDF Extraction Error: {e}")
                raise Exception("Failed to extract text from PDF for Groq")
                
            prompt = f"""
            You are an expert HR data extractor. Parse the following resume text and extract the data into a JSON object with this EXACT schema.
            
            CRITICAL RULES:
            1. If the resume does not have an explicit professional summary, YOU MUST write a short 2-3 sentence professional summary based on their experience for the "about" field.
            2. YOU MUST generate a short 3-5 word professional headline for the "headline" field based on their most recent role.
            3. Look very carefully for the phone number, it may be in the header or footer.
            
            SCHEMA:
            {{
                "headline": "A professional headline, e.g. Senior Software Engineer",
                "about": "A short professional summary of the candidate (generate one if missing)",
                "years_of_experience": 5,
                "phone": "e.g. +1234567890",
                "city": "e.g. San Francisco",
                "github_url": "https://github.com/...",
                "linkedin_url": "https://linkedin.com/in/...",
                "skills": [
                    {{"name": "Python", "proficiency": "expert"}}
                ],
                "experiences": [
                    {{
                        "title": "Software Engineer",
                        "company_name": "Tech Corp",
                        "start_date": "2020-01-01",
                        "end_date": "2023-01-01",
                        "description": "Built cool things."
                    }}
                ],
                "education": [
                    {{
                        "degree": "BSc Computer Science",
                        "institution_name": "University",
                        "start_date": "2015-01-01",
                        "end_date": "2019-01-01"
                    }}
                ],
                "certifications": [
                    {{
                        "name": "AWS Certified Solutions Architect",
                        "issuing_organization": "Amazon",
                        "issue_date": "2021-01-01",
                        "expiration_date": "2024-01-01"
                    }}
                ]
            }}
            Return ONLY valid JSON. Do not include any prefix text like "Here is the extracted data" or markdown formatting. Start immediately with {{ and end with }}. If a field is missing, leave it empty or null.
            
            RESUME TEXT:
            {text[:15000]}
            """
            
            data = llm.generate_json(prompt, model_name='llama-3.3-70b-versatile')
            if not data:
                raise Exception("Groq returned empty JSON")
                
        else:
            # Fallback to Gemini Vision if Groq is not configured
            client = getattr(llm, 'client', None)
            if not client:
                logger.error("No LLM client configured")
                return
                
            gemini_file = client.files.upload(file=file_path)
            prompt = """
            Parse this resume and extract the data into a JSON object with this EXACT schema.
            
            CRITICAL RULES:
            1. If the resume does not have an explicit professional summary, YOU MUST write a short 2-3 sentence professional summary based on their experience for the "about" field.
            2. YOU MUST generate a short 3-5 word professional headline for the "headline" field based on their most recent role.
            3. Look very carefully for the phone number, it may be in the header or footer.
            
            SCHEMA:
            {
                "headline": "A professional headline, e.g. Senior Software Engineer",
                "about": "A short professional summary of the candidate (generate one if missing)",
                "years_of_experience": 5,
                "phone": "e.g. +1234567890",
                "city": "e.g. San Francisco",
                "github_url": "https://github.com/...",
                "linkedin_url": "https://linkedin.com/in/...",
                "skills": [
                    {"name": "Python", "proficiency": "expert"}
                ],
                "experiences": [
                    {
                        "title": "Software Engineer",
                        "company_name": "Tech Corp",
                        "start_date": "2020-01-01",
                        "end_date": "2023-01-01",
                        "description": "Built cool things."
                    }
                ],
                "education": [
                    {
                        "degree": "BSc Computer Science",
                        "institution_name": "University",
                        "start_date": "2015-01-01",
                        "end_date": "2019-01-01"
                    }
                ],
                "certifications": [
                    {
                        "name": "AWS Certified Solutions Architect",
                        "issuing_organization": "Amazon",
                        "issue_date": "2021-01-01",
                        "expiration_date": "2024-01-01"
                    }
                ]
            }
            Return ONLY valid JSON.
            """
            
            response = None
            for attempt in range(2):
                try:
                    response = client.models.generate_content(
                        model='gemini-2.5-flash',
                        contents=[gemini_file, prompt]
                    )
                    break
                except Exception as e:
                    if '429' in str(e) and attempt == 0:
                        time.sleep(60)
                    else:
                        raise e
            
            try:
                client.files.delete(name=gemini_file.name)
            except Exception:
                pass
                
            text = response.text.strip()
            if text.startswith('```json'):
                text = text[7:-3]
            elif text.startswith('```'):
                text = text[3:-3]
            data = json.loads(text.strip())

        # Save data to profile
        if isinstance(data, dict):
            if data.get('headline'): profile.headline = str(data['headline'])
            if data.get('about'): profile.about = str(data['about'])
            if data.get('github_url'): profile.github_url = str(data['github_url'])
            if data.get('linkedin_url'): profile.linkedin_url = str(data['linkedin_url'])
            if data.get('phone'): profile.phone = str(data['phone'])
            if data.get('city'): profile.city = str(data['city'])
            if 'years_of_experience' in data and data['years_of_experience'] is not None:
                try:
                    profile.years_of_experience = int(data['years_of_experience'])
                except (ValueError, TypeError):
                    pass
            profile.save()
            
            import re
            def _parse_date(d):
                if not d: return None
                d = str(d).strip().lower()
                if d in ('present', 'current', 'now'): return None
                if re.match(r'^\d{4}-\d{2}-\d{2}$', d): return d
                if re.match(r'^\d{4}-\d{2}$', d): return f"{d}-01"
                if re.match(r'^\d{4}$', d): return f"{d}-01-01"
                return None
            
            def _safe_str(val, max_len=None, default=''):
                if val is None: return default
                s = str(val).strip()
                if max_len: return s[:max_len]
                return s

            skills_list = data.get('skills')
            if isinstance(skills_list, list):
                for skill in skills_list:
                    if isinstance(skill, str):
                        name = _safe_str(skill, 100)
                        if name:
                            CandidateSkill.objects.get_or_create(candidate=profile, name=name, defaults={'proficiency': 'intermediate'})
                    elif isinstance(skill, dict):
                        name = _safe_str(skill.get('name'), 100)
                        if not name: continue
                        prof = _safe_str(skill.get('proficiency'), 50, 'intermediate')
                        if not prof: prof = 'intermediate'
                        CandidateSkill.objects.get_or_create(candidate=profile, name=name, defaults={'proficiency': prof})
                
            exp_list = data.get('experiences')
            if isinstance(exp_list, list):
                for exp in exp_list:
                    if not isinstance(exp, dict): continue
                    title = _safe_str(exp.get('title'), 255)
                    company = _safe_str(exp.get('company_name'), 255)
                    if not title or not company: continue
                    sd = _parse_date(exp.get('start_date')) or '2000-01-01'
                    ed = _parse_date(exp.get('end_date'))
                    desc = _safe_str(exp.get('description'))
                    try:
                        Experience.objects.get_or_create(
                            candidate=profile, title=title, company_name=company,
                            defaults={'start_date': sd, 'end_date': ed, 'description': desc}
                        )
                    except Exception:
                        pass
                
            edu_list = data.get('education')
            if isinstance(edu_list, list):
                for edu in edu_list:
                    if not isinstance(edu, dict): continue
                    degree = _safe_str(edu.get('degree'), 255)
                    inst = _safe_str(edu.get('institution_name'), 255)
                    if not degree or not inst: continue
                    sd = _parse_date(edu.get('start_date')) or '2000-01-01'
                    ed = _parse_date(edu.get('end_date'))
                    try:
                        Education.objects.get_or_create(
                            candidate=profile, degree=degree, institution_name=inst,
                            defaults={'start_date': sd, 'end_date': ed}
                        )
                    except Exception:
                        pass
                
            cert_list = data.get('certifications')
            if isinstance(cert_list, list):
                for cert in cert_list:
                    if not isinstance(cert, dict): continue
                    name = _safe_str(cert.get('name'), 255)
                    org = _safe_str(cert.get('issuing_organization'), 255)
                    if not name or not org: continue
                    issue = _parse_date(cert.get('issue_date'))
                    expir = _parse_date(cert.get('expiration_date'))
                    try:
                        Certification.objects.get_or_create(
                            candidate=profile, name=name, issuing_organization=org,
                            defaults={'issue_date': issue, 'expiration_date': expir}
                        )
                    except Exception:
                        pass

        from apps.notifications.models import Notification
        Notification.objects.create(
            user=profile.user,
            type='system',
            title='Resume Analyzed 🚀',
            message='Our AI has successfully analyzed your resume and updated your profile with your skills, experience, and education!',
            action_url='/candidate/profile'
        )
            
    except Exception as e:
        logger.error(f"Failed to parse resume with LLM: {e}")
        from apps.candidates.models import CandidateProfile
        profile = CandidateProfile.objects.filter(id=candidate_id).first()
        if profile:
            from apps.notifications.models import Notification
            Notification.objects.create(
                user=profile.user,
                type='system',
                title='Resume Analysis Delayed',
                message='We could not analyze your resume right now because the AI servers are experiencing extreme high demand. Please try again later.',
                action_url='/candidate/profile'
            )
